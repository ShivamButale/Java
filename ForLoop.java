public class ForLoop {
    public static void main(String[] args) {
        for(int x=10; x>1; x--) {
            System.out.println("Value of x is "+x);
        }
        System.out.println("\n");
        for(int y=1; y<=10;y++) {
            System.out.println("Value of y is "+y);
        }
    }
}